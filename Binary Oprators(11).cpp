//CPP program to demonstrate the Binary Oprators
#include <iostream>
using namespace std;

int main() 
{
  int a=8, b=3;
  //addition oprator
  cout<<"a+b = "<<(a+b)<<endl;
  //subtraction operator
  cout<<"a-b = "<<(a-b)<<endl;
  //multiplication operator
   cout<<"a*b = "<<(a*b)<<endl;
  //division operator
   cout<<"a/b = "<<(a/b)<<endl;
  //modulos operator
   cout<<"a%b = "<<(a % b)<<endl;
   
   
    return 0;
}
